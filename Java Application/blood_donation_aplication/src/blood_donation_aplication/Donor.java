    public boolean isValidEmail(String email)
    {
        boolean flag = false;
        if(!email.isEmpty())
        {
            if(email.indexOf("@")!=-1 && email.indexOf(".")!=-1)
            {
                flag=true;
            }
        }
        return flag;
    }
    
    
