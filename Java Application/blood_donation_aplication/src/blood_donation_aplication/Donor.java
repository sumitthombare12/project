    public boolean isValidateAdhar(String adhar)
    {
        boolean flag = false;
        
        try
        {
            if(!adhar.isEmpty())
            {
                if(adhar.length()==12)
                {
                    long Adhar = Long.parseLong(adhar);
                    flag = true;
                }
                else
                {
                    System.out.println("Adhar Digits are not valid");
                }
            }
        }
        catch(Exception ex)
        {
            System.out.println("Please enter valid adhar No.");
        }
        return flag;
    }
    
